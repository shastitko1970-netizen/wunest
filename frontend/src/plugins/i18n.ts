export const i18n = createI18n({
  legacy: false,
  locale: detectLocale(),
  fallbackLocale: 'en',
  messages,
})

function detectLocale(): string {
  const saved = localStorage.getItem('nest:locale')
  if (saved === 'ru' || saved === 'en') return saved
  return navigator.language?.startsWith('ru') ? 'ru' : 'en'
}
